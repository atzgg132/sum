/**
 * A simple Java program to add two hardcoded numbers and display the result.
 */
public class Sum {
    public static void main(String[] args) {
        // Hardcoded numbers
        int num1 = 25;
        int num2 = 30;
        
        // Calculate the sum
        int sum = num1 + num2;
        
        // Display the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}

